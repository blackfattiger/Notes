#include<iostream>
using namespace std;
int main(){
    //1.短整型 2字节 (-32768~32767)
    short a = 10000;
    //2.整型    4字节（）
    int b = 100000;
    //3.长整型
    long c = 100000000;
    //4.长长整型
    long long d = 10000000000;
    cout<<a<<endl;
    cout<<b<<endl;
    cout<<c<<endl;
    cout<<d<<endl;
    return 0 ;
} 
